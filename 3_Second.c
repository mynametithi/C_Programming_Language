//Arithmetic Operators 
#include<stdio.h>
int main(){
int a=7;
int b=9;
int z;
z=b*a;   //b*a=z (not allowed)
printf("The value of z is : %d \n",z);
printf("The value of a+b is : %d \n",a+b);
printf("The value of a-b is : %d \n",a-b);
printf("The value of a*b is : %d \n",a*b);
printf("The value of a/b is : %d \n",a/b);
    return 0;
}